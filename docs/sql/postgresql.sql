-- PostgreSQL DDL for JobRunr Control parameter sets table
-- Create this table manually before using external parameter storage

CREATE TABLE IF NOT EXISTS "JOBRUNR_CONTROL_PARAMETER_SETS" (
    "ID" VARCHAR(36) PRIMARY KEY NOT NULL,
    "JOB_TYPE" VARCHAR(500) NOT NULL,
    "PARAMETERS_JSON" JSONB NOT NULL,
    "CREATED_AT" TIMESTAMP NOT NULL,
    "UPDATED_AT" TIMESTAMP NOT NULL,
    "VERSION" BIGINT
);

CREATE INDEX IF NOT EXISTS idx_param_set_job_type ON "JOBRUNR_CONTROL_PARAMETER_SETS"("JOB_TYPE");
CREATE INDEX IF NOT EXISTS idx_param_set_created ON "JOBRUNR_CONTROL_PARAMETER_SETS"("CREATED_AT");
CREATE INDEX IF NOT EXISTS idx_param_set_updated ON "JOBRUNR_CONTROL_PARAMETER_SETS"("UPDATED_AT");

COMMENT ON TABLE "JOBRUNR_CONTROL_PARAMETER_SETS" IS 'Stores job parameter sets for JobRunr Control external parameter storage';
COMMENT ON COLUMN "JOBRUNR_CONTROL_PARAMETER_SETS"."ID" IS 'Unique identifier (UUID)';
COMMENT ON COLUMN "JOBRUNR_CONTROL_PARAMETER_SETS"."JOB_TYPE" IS 'Fully qualified job class name';
COMMENT ON COLUMN "JOBRUNR_CONTROL_PARAMETER_SETS"."PARAMETERS_JSON" IS 'Job parameters stored as JSON';
COMMENT ON COLUMN "JOBRUNR_CONTROL_PARAMETER_SETS"."CREATED_AT" IS 'Timestamp when the parameter set was created';
COMMENT ON COLUMN "JOBRUNR_CONTROL_PARAMETER_SETS"."UPDATED_AT" IS 'Timestamp when the parameter set was last updated';
COMMENT ON COLUMN "JOBRUNR_CONTROL_PARAMETER_SETS"."VERSION" IS 'Optimistic locking version';

CREATE TABLE IF NOT EXISTS "JOBRUNR_CONTROL_BATCH_RECAP" (
    "BATCH_JOB_ID" VARCHAR(36) NOT NULL,
    "CHILD_JOB_ID" VARCHAR(36) NOT NULL,
    "COUNTER_NAME" VARCHAR(255) NOT NULL,
    "COUNTER_VALUE" BIGINT NOT NULL DEFAULT 0,
    PRIMARY KEY ("BATCH_JOB_ID", "CHILD_JOB_ID", "COUNTER_NAME")
);
CREATE INDEX IF NOT EXISTS idx_batch_recap_agg ON "JOBRUNR_CONTROL_BATCH_RECAP"("BATCH_JOB_ID", "COUNTER_NAME", "COUNTER_VALUE");

CREATE TABLE IF NOT EXISTS "JOBRUNR_CONTROL_BATCH_MESSAGES" (
    "ID" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "BATCH_JOB_ID" VARCHAR(36) NOT NULL,
    "CREATED_AT" TIMESTAMP NOT NULL,
    "LEVEL" VARCHAR(20) NOT NULL,
    "MESSAGE" TEXT,
    "STACK_TRACE" TEXT
);

CREATE INDEX IF NOT EXISTS idx_batch_msg_created ON "JOBRUNR_CONTROL_BATCH_MESSAGES"("BATCH_JOB_ID", "CREATED_AT");
CREATE INDEX IF NOT EXISTS idx_batch_msg_filter ON "JOBRUNR_CONTROL_BATCH_MESSAGES"("BATCH_JOB_ID", "LEVEL", "CREATED_AT");

COMMENT ON TABLE "JOBRUNR_CONTROL_BATCH_RECAP" IS 'Recap counters per child job in a batch';
COMMENT ON COLUMN "JOBRUNR_CONTROL_BATCH_RECAP"."BATCH_JOB_ID" IS 'Batch job identifier (UUID)';
COMMENT ON COLUMN "JOBRUNR_CONTROL_BATCH_RECAP"."CHILD_JOB_ID" IS 'Child job identifier (UUID)';
COMMENT ON COLUMN "JOBRUNR_CONTROL_BATCH_RECAP"."COUNTER_NAME" IS 'Recap counter name';
COMMENT ON COLUMN "JOBRUNR_CONTROL_BATCH_RECAP"."COUNTER_VALUE" IS 'Counter value for one child job';

COMMENT ON TABLE "JOBRUNR_CONTROL_BATCH_MESSAGES" IS 'Batch job log and exception messages';
COMMENT ON COLUMN "JOBRUNR_CONTROL_BATCH_MESSAGES"."BATCH_JOB_ID" IS 'Batch job identifier (UUID)';
COMMENT ON COLUMN "JOBRUNR_CONTROL_BATCH_MESSAGES"."CREATED_AT" IS 'Timestamp when the message was created';
COMMENT ON COLUMN "JOBRUNR_CONTROL_BATCH_MESSAGES"."LEVEL" IS 'Message level (INFO, WARNING, ERROR, EXCEPTION)';
